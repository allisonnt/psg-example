print("Cómo lavar los platos")

print("Reúne los materiales necesarios:")
print("- Esponja suave con doble cara (una suave y una mas dura).")
print("- Detergente líquido para platos.")
print("- Guantes de goma (opcional, para proteger tus manos).")
print("- Estropajo o cepillo pequeño (para manchas difíciles).")
print("- Secador de platos, rejilla o un paño limpio.")
print("- Agua caliente (mejor para remover grasa).")

print("Ordena los platos sucios:")
print("- Quita los restos de comida de cada plato antes de lavarlos.")
print("- Separa los utensilios por tipo: vasos, cubiertos, platos, ollas.")

print("Llena el fregadero con agua caliente y un poco de detergente.")
print("- Usa el agua para dejar en remojo los objetos con grasa o suciedad pegada.")

print("Lava los utensilios en este orden:")
print("a) Vasos y copas (para evitar que se engrasen con el agua sucia).")
print("b) Cubiertos (cucharas, tenedores, cuchillos).")
print("c) Platos y tazones.")
print("d) Ollas, sartenes y bandejas al final (son los más grasosos).")

print("Técnica de lavado:")
print("- Usa la parte suave de la esponja para cristalería y platos delicados.")
print("- Usa la parte abrasiva para ollas y residuos difíciles.")
print("- Asegúrate de lavar bien los bordes, el reverso y los mangos.")

print("Enjuaga bien con agua limpia y caliente.")
print("- No dejes residuos de jabón, ya que pueden ser dañinos para la salud.")

print("Seca los platos:")
print("- Usa un paño de cocina limpio y seco.")
print("- También puedes dejarlos escurrir en una rejilla o secador de platos.")

print("Limpieza final:")
print("- Limpia la esponja, el fregadero y las superficies de trabajo.")
print("- Lava tus manos si no usaste guantes.")

print("Listo! Ahora tienes platos limpios y brillantes.")